// ===== Affiche fédération ou communes =====
var fed = document.getElementById('s5-federation'),
    com = document.getElementById('s5-communes'),
    arrow1 = document.getElementById('choix1'),
    arrow2 = document.getElementById('choix2');

arrow1.addEventListener('click', function() {
  check_affichage("federation");

});

arrow2.addEventListener('click', function() {
  check_affichage("communes");

});

function check_affichage(p1) {
  if (p1 == "federation") {
    if (fed.classList.contains("hidden")) {
      fed.style.display = "flex";
      affiche_section(p1);
    } else {
      fed.classList.add("hidden");
      fed.style.display = "none";
    }
  } else if (p1 == "communes") {
    if (com.classList.contains("hidden")) {
      affiche_section(p1);
      com.style.display = "flex";
    } else {
      com.classList.add("hidden");
      com.style.display = "none";
    }
  } else {
      console.info('ça a epic fail');
  }
};

function affiche_section(p1) {
  if (p1 == "federation") {
    fed.classList.remove("hidden");

  } else if (p1 == "communes") {
    //enlever les changements
    com.classList.remove("hidden");
  } else {
      console.info('ça a epic fail');
  }
};

